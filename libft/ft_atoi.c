#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	v;
	int	neg;

	neg = 1;
	i = 0;
	while (s[i] == ' ' || (s[i] >= '\t' && s[i] <= '\r'))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			neg *= -1;
		i++;
	}
	v = 0;
	while ('0' <= s[i] && s[i] <= '9')
	{
		v *= 10;
		v = v + s[i] - '0';
		i++;
	}
	v *= neg;
	return (v);
}
